<h2>Paso 3: Presenta la solicitud en línea</h2>
<p>Todos los trámites de Residencia Temporal se presentan en el portal del SERMIG: <strong>tramites.extranjeria.gob.cl</strong>. El proceso es 100% digital — no necesitas ir a una oficina para presentar la solicitud.</p>
<ol>
  <li>Crea una cuenta en el portal.</li>
  <li>Selecciona "Nueva solicitud" → "Residencia Temporal".</li>
  <li>Elige la categoría que corresponde a tu situación.</li>
  <li>Sube los documentos en formato PDF.</li>
  <li>Paga el arancel (varía según categoría).</li>
  <li>Guarda el comprobante de radicación.</li>
</ol>

<h2>Paso 4: Ingresa a Chile y espera la resolución</h2>
<p>Puedes presentar la solicitud desde Chile (ingresando como turista) o desde el exterior. En la práctica, la mayoría lo hace estando en Chile:</p>
<ul>
  <li>Ingresas como turista con tu pasaporte.</li>
  <li>Presentas la solicitud en línea.</li>
  <li>Con el comprobante de radicación, tu situación queda regularizada mientras el SERMIG resuelve.</li>
  <li>El plazo de resolución varía entre 2 y 8 meses según la categoría.</li>
</ul>

<h2>Paso 5: Obtén tu cédula de identidad de extranjero</h2>
<p>Cuando el SERMIG aprueba tu solicitud, te notifica y debes ir al <strong>Registro Civil</strong> a sacar tu cédula de identidad de extranjero. La cédula incluye tu RUT permanente — el documento de identidad que usarás en Chile para todo.</p>
<p>→ Ver: <a href="/vivir-en-chile/cedula-extranjero">Cédula de identidad de extranjero</a></p>

