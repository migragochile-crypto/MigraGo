<h2>Paso 2: Reúne los documentos</h2>
<p>Los documentos básicos que necesitas — independiente de la categoría — son:</p>
<ul>
  <li><strong>Pasaporte vigente</strong> (sin apostilla — se presenta directamente).</li>
