<p>El primer paso es identificar qué categoría de Residencia Temporal te aplica. Chile tiene varias vías:</p>

<h3>Vía MERCOSUR (más fácil para la mayoría)</h3>
<p>Si eres ciudadano de Argentina, Bolivia, Brasil, Chile, Colombia, Ecuador, Paraguay, Perú o Uruguay, puedes solicitar la Residencia Temporal por MERCOSUR — sin necesitar contrato de trabajo ni vínculos familiares en Chile. Solo acreditas tu nacionalidad.</p>
