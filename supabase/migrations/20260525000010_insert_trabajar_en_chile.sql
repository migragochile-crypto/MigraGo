<p>Mientras el SERMIG resuelve tu solicitud de Residencia Temporal, el <strong>comprobante de radicación</strong> te permite trabajar en muchos casos. Sin embargo, algunos empleadores — especialmente grandes empresas o el sector público — pueden exigir la cédula vigente. Verifica con tu empleador.</p>

<h3>Visa de turista (sin permiso de trabajo)</h3>
<p>Con visa de turista o permiso de turismo <strong>no puedes trabajar legalmente</strong>. Si trabajas en esta condición, tanto tú como el empleador quedan expuestos a multas del SERMIG.</p>

