  <li><strong>Razones humanitarias:</strong> para situaciones de vulnerabilidad especial.</li>
</ul>

<h2>Ingreso a Chile como turista</h2>
<p>Los ciudadanos colombianos no requieren visa para ingresar a Chile como turistas. Puedes entrar con pasaporte colombiano vigente. La estadía de turista permite permanecer hasta 90 días, prorrogables, pero <strong>no autoriza a trabajar</strong>. Para trabajar necesitas la Residencia Temporal.</p>

<h2>Documentos que necesitas apostillar</h2>
<p>Colombia es signatario del Convenio de La Haya — los documentos se apostillan ante la <strong>Cancillería colombiana</strong> en línea o presencialmente:</p>
<ul>
  <li><strong>Pasaporte colombiano vigente</strong> (no necesita apostilla — se presenta directo).</li>
  <li><strong>Registro civil de nacimiento:</strong> emitido por la Registraduría Nacional del Estado Civil, apostillado.</li>
  <li><strong>Certificado judicial de antecedentes penales:</strong> emitido por la Policía Nacional de Colombia, apostillado.</li>
  <li>Si aplicas por pareja/hijo: acta de matrimonio o acta de nacimiento del hijo, apostilladas.</li>
</ul>
<p>La apostilla en Colombia se tramita en línea en <strong>cancilleria.gov.co</strong> — el proceso puede ser inmediato o tomar 1–3 días hábiles. Ver: <a href="/vivir-en-chile/apostilla-por-pais">Apostilla por país para trámites en Chile</a></p>

<h2>Antecedentes penales colombianos: dónde tramitarlos</h2>
<p>El certificado de antecedentes penales para trámites internacionales se obtiene en la <strong>Policía Nacional de Colombia</strong> (policia.gov.co). Hay dos tipos:</p>
<ul>
  <li><strong>Para uso en el exterior:</strong> es el correcto para el SERMIG — deja claro que es para trámites fuera de Colombia.</li>
  <li>Una vez obtenido, se lleva a la Cancillería para apostillar.</li>
  <li>El SERMIG exige que los antecedentes penales no tengan más de <strong>90 días de antigüedad</strong> al presentarlos.</li>
</ul>

<h2>Proceso paso a paso para solicitar la Residencia Temporal MERCOSUR</h2>
<ol>
  <li>Apostilla tus documentos en Colombia (puedes hacerlo antes de viajar o estando en Chile).</li>
  <li>Ingresa a Chile con tu pasaporte colombiano.</li>
  <li>Crea una cuenta en <strong>tramites.extranjeria.gob.cl</strong>.</li>
  <li>Selecciona "Residencia Temporal" → "MERCOSUR".</li>
  <li>Sube los documentos requeridos en formato PDF.</li>
  <li>Paga el arancel correspondiente.</li>
  <li>Recibirás el comprobante de radicación — ya puedes trabajar en muchos empleos con este documento.</li>
  <li>Espera la resolución del SERMIG (2–5 meses para MERCOSUR).</li>
  <li>Con la aprobación, saca tu cédula de identidad de extranjero en el Registro Civil.</li>
</ol>

<h2>Vida cotidiana en Chile para colombianos</h2>

<h3>Trabajo</h3>
<p>Los colombianos trabajan en una amplia variedad de sectores en Chile. Los más comunes son comercio, gastronomía, construcción y servicios. Con estudios superiores hay buenas oportunidades en tecnología, salud y finanzas. El sueldo mínimo es $500.000 CLP en 2026. Ver: <a href="/vivir-en-chile/trabajar-en-chile">Trabajar en Chile siendo extranjero</a></p>

<h3>Cuenta bancaria</h3>
<p>Con cédula de extranjero vigente, BancoEstado (CuentaRUT) es la opción más accesible. MACH (app de BancoEstado) permite abrir cuenta antes de tener la cédula, solo con RUT. Ver: <a href="/vivir-en-chile/cuenta-bancaria">Cuenta bancaria para extranjeros</a></p>

<h3>Salud</h3>
<p>Con contrato de trabajo formal te afilias automáticamente a Fonasa (público) o eliges una Isapre (privado). El 7% de tu sueldo se descuenta mensualmente. Ver: <a href="/vivir-en-chile/fonasa-isapre">Fonasa vs Isapre para extranjeros</a></p>

<h3>Diferencias culturales a considerar</h3>
<ul>
  <li><strong>Vocabulario:</strong> mismo idioma pero palabras diferentes — "guagua" es bebé, "palta" es aguacate, "plata" es dinero, "pololo/a" es novio/a.</li>
  <li><strong>Clima:</strong> Santiago tiene inviernos fríos con heladas (0°C a 5°C en julio) — diferente al clima colombiano.</li>
  <li><strong>Comida:</strong> la dieta chilena es más sencilla que la colombiana. Los mercados y ferias ofrecen productos accesibles.</li>
</ul>

