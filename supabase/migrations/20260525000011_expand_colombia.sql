<h2>Ventaja clave: acceso MERCOSUR</h2>
<p>Colombia es miembro del Acuerdo de Residencia del MERCOSUR. Esto significa que los colombianos tienen acceso a la <strong>categoría de Residencia Temporal MERCOSUR</strong>, la vía más directa y sencilla para regularizarse en Chile:</p>
<ul>
  <li>No necesitas contrato de trabajo previo.</li>
  <li>No necesitas cónyuge ni hijo chileno.</li>
  <li>Solo acreditas tu nacionalidad colombiana (pasaporte o cédula).</li>
  <li>La visa dura 2 años, renovable.</li>
  <li>Después de 2 años (o 1 en casos especiales) puedes pedir la Residencia Definitiva.</li>
</ul>
